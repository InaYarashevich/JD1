package home_work_1;

public class ClassMathDemo {
    public static void main(String[] args) {
        System.out.println("Максимальное из двух чисел: " + Math.max(5, 9));
        System.out.println("Значение по модулю: " + Math.abs(-12.4D));
        System.out.println("Возведение в степень: " + Math.pow(2, 5));
        System.out.println("Рандомное число: " + Math.random());
        System.out.println("Округление в меньшую сторонуЖ " + Math.floor(5.9));
        System.out.println("Кубический корень: " + Math.cbrt(81));
        System.out.println("Округление: " + Math.round(12.99999));
        System.out.println("Натуральный логарифм по основанию e и аргументу: " + Math.exp(100));
        System.out.println("Округление к инту: " + Math.rint(9.8));
    }
}
